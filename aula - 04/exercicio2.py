#Modifique a busca linear para mostrar quantas vezes o valor aparece na lista.

def busca_linear (lista, valor):
    for i in range(len(lista)):
        if (lista[i] == valor):
            cont=lista.count(valor)
            return cont
    return

numeros=(1,2,7,34,56,7,7)

print(busca_linear(numeros, 7))
print(busca_linear(numeros, 57))
